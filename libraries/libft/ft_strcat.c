#include "libft.h"

void	ft_strcat(char *src, char *add)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (src[i])
		i++;
	while (add[j])
		src[i++] = add[j++];
	src[i] = '\0';
}
